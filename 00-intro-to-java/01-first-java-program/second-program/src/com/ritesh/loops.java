package com.ritesh;

public class loops {
    public static void main(String[] args) {
        
        // While Loop
        int count = 1;
        while (count != 5) {
            System.out.println(count);
            count++;
        }

        // For loop
        for(int county = 1; county != 5; county++){
            System.out.println(county);
        }

    }
}